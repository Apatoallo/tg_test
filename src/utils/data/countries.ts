export const getValueByLabel = (
  label: string | undefined,
  language: string,
): string => {
  if (!label) {
    return '';
  }
  const data = language === lang.en ? countriesCodeEn : countriesCodeAr;
  const res = data.find(i => i.label === label);
  return res ? res.value : '';
};

